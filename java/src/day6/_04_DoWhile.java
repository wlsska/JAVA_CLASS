package day6;

public class _04_DoWhile {

	public static void main(String[] args) {
		int num1 = 6;
		do {
			System.out.println(num1); //현재 숫자 출력
			num1++;
		}while(num1 <= 5);
		// while과 dowhile의 차이점
		// while은 조건이 false라면 한 번도 실행되지 않음
		// dowhile : 조건이 false라도 최소 1번 실행됨 
		
		
		}

	}


